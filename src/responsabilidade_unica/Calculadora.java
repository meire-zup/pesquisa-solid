package responsabilidade_unica;

// Classe responsável por realizar as operações
public class Calculadora {

    public double somaNumeros(double numero1, double numero2) {

        return numero1 + numero2;

    }

    public double subtraiNumeros(double numero1, double numero2) {

        return numero1 - numero2;

    }

    public double multiplicaNumeros(double numero1, double numero2) {

        return numero1 * numero2;

    }

    public double divideNumeros(double numero1, double numero2) {

        return numero1 / numero2;

    }

}
